// Share functionality
function sharePage() {
    if (navigator.share) {
        navigator.share({
            title: document.title,
            text: 'ChatGPT Enterprise 企業導入方案 - TPIsoftware',
            url: window.location.href
        }).then(() => {
            console.log('Thanks for sharing!');
        }).catch(err => {
            console.error('Error sharing:', err);
        });
    } else {
        copyToClipboard(window.location.href);
        alert('連結已複製到剪貼簿');
    }
}

